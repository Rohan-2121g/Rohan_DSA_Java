package com.Rohan;

import java.util.LinkedList;

public class FAST_TASK_8 {

	public static void main(String[] args) {
        LinkedList<String> colors = new LinkedList<>(); // create a LinkedList to store colors
        colors.add("Orange");                              // add colors to the list
        colors.add("Neon");
        colors.add("Pink");
        colors.add("Violet");

        colors.addLast("Black");                       // insert "Black" at the end of the list
        System.out.println(colors);                     // print the updated list
    }
}


